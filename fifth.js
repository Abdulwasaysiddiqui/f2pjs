// ARRAY
let fruits = ["APPLE" , "MANGO" , "ORANGE" , "PINEAPPLE"];
console.log(fruits);
console.log(fruits.length);

// METHODS OF AN ARRAY
// PUSH LAST INDEX ME ADD KRTA H
console.log(fruits.push("banana"));
console.log(fruits);

//pop last index ko remove krta h
console.log(fruits.pop());
console.log(fruits);

// shift first index ko remove krta h
console.log(fruits.shift());
console.log(fruits);
//unshift fisrt index ko add krta h
console.log(fruits.unshift());
console.log(fruits);

//slice
console.log(fruits.slice(1,4));