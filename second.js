 let z = 50;
 // post INCREMEBNT
 
 console.log(z++);  // z = z + 1 => 51
 console.log(z);  // 51
 // Pre Increment
 console.log(++z);  // z = z + 1 => 51 + 1 => 52
 
 z += 20; // 72
 
 console.log(z); //51
 
 // Decrement
 
 let y = 60
 
 console.log(y--);  // z = z + 1 => 51
 console.log(y);  // 51
 // Pre Increment
 console.log(--y);  // z = z + 1 => 51 + 1 => 52
 
 y -= 20; // 72
 
 console.log(y); //51
 
 
